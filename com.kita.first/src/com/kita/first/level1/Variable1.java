package com.kita.first.level1;

public class Variable1 {
	public static void main(String[] args) {
		char v1 = 'A';
		
		byte v2;
		short v3;
		int v4;  // 정수형 기본타입
		v4 = 3;
		System.out.println(v4);
		v4 = 5;
		System.out.println(v4);
		long v5 = 214748364234L;
		
		float v6 = 1.2f;
		double v7 = 2.3; // 실수형 기본타입
		
		boolean v8;
		v8 = true;
		v8 = false;
	}
}
